                    <div class="col-md-10 col-lg-8 col-xl-7">
                        <h2 class="section-heading">Un autre point de vue intéressant...</h2>
                        <p style="text-decoration: underline;"><u>Lola :</u></p>
                        <div class="texte-marge3">
                        <p>"Première question qui va être très générale : On va vous laisser vous présenter et nous décrire un peu votre métier et en quoi ça consiste" </p>
                        </div>
                        <p style="text-decoration: underline;"><u>Conseillère funéraire :</u></p>
                        <div class="texte-marge3">
                            "Je m’appelle Sarah, cela fait 2 ans que je suis conseillère funéraire.
                            J’ai fait une reconversion professionnelle car à l’origine je ne suis pas du tout de ce domaine là. J’étais dans la coiffure pendant 7 ans, mais suite à une allergie, et d’avoir frôlé une septicémie, j’ai dû tout arrêter. Pourquoi le monde du funéraire ? Je ne sais pas, j’ai eu plusieurs décès dans ma famille etc… J’aimais beaucoup ce métier d’accompagnement donc je me suis dirigée là dedans. J’ai donc entamé une formation de 3 mois en tant que conseillère funéraire.
                            J’ai été diplômée et j’ai donc intégré le groupe Funecap chez Roc Eclerc il y a deux ans. Le métier de conseiller funéraire est un métier où l’on touche un peu à tout. Le métier principal, on reçoit les familles qui perdent quelqu’un, on organise donc un entretien de famille et on organise l'entièreté des obsèques. Ca part de l’accueil téléphonique à la réalisation des obsèques le jour J. Nous, on est rattachés à une chambre funéraire donc on a la visibilité sur les défunts, donc on peut habiller, maquiller, mais on ne s’occupe pas de la toilette.""
                            </p>
                        </div>
                        <h2 class="section-heading">C'est parfois compliqué...</h2>
                        <p style="text-decoration: underline;"><u>Lola :</u></p>
                        <div class="texte-marge3">
                            "Comment suite à cette reconversion vous vous êtes “mise dans le bain” de ce monde là qui est quand même un peu particulier et dans lequel on peut avoir pas mal d’appréhensions au début ?"</p>
                        <p style="text-decoration: underline;"><u>Sarah :</u></p>
                        <div class="texte-marge3">
                            "Je suis très empathique, c’est un métier qui en requiert beaucoup, et il faut aussi savoir prendre beaucoup de distance. On a pas des cas faciles tous les jours. Quand c’est la petite mamie de 90 ans, la famille est quand même préparée. Quand c’est des jeunes de 11, 15 ans accidents de la route etc… c’est plus compliqué. On se détache au maximum et on approche les familles différemment, ça dépend des cas."</p>
                        <p style="text-decoration: underline;"><u>Maël :</u></p>
                        <div class="texte-marge3">
                            "Est-ce que ça vous touche personnellement quand c’est des cas un peu plus compliqués ?"</p>
                        <p style="text-decoration: underline;"><u>Sarah :</u></p>
                        <div class="texte-marge3">
                            "Ça peut toucher oui, tout dépend des situations mais il y a forcément des cas qui nous touchent un peu plus."</p>
                        <p style="text-decoration: underline;"><u>Lola :</u></p>
                        <div class="texte-marge3">
                            "Est-ce que vous avez un accompagnement psychologique pour faire face à ces difficultés éventuellement ?"</p>
                        <p style="text-decoration: underline;"><u>Sarah :</u></p>
                        <div class="texte-marge3">
                            "Oui, on a une cellule psychologique qu’on peut joindre 24h/24 7jours sur 7."</p>
                        <p style="text-decoration: underline;"><u>Lola :</u></p>
                        <div class="texte-marge3">
                            "Quelles sont les difficultés rencontrées au sein de votre métier ?"</p>
                        <p style="text-decoration: underline;"><u>Sarah :</u></p>
                        <div class="texte-marge3">
                            "Cela peut être les conflits familiaux, ou si le défunt n’est pas visible. Après en soit des difficultés il n’y en a pas tellement."</p>
                   
                        <h2 class="section-heading">Philosophie et discussion</h2>
                        <p style="text-decoration: underline;"><u>Lola :</u></p>
                        <div class="texte-marge3">
                            "Pour vous, un cimetière est-il avant tout un lieu de vie ?"</p>
                        <p style="text-decoration: underline;"><u>Sarah :</u></p>
                        <div class="texte-marge3">
                            "Je dirai que oui, ça peut."</p>
                        <p style="text-decoration: underline;"><u>Lola :</u></p>
                        <div class="texte-marge3">
                            "Quelle est votre relation avec la mort ?"</p>
                        <p style="text-decoration: underline;"><u>Sarah :</u></p>
                        <div class="texte-marge3">
                            "J’avais beaucoup peur de la mort, je pense comme tout le monde.
                            J’ai eu beaucoup de personnes de mon entourage qui tenaient ces mots : “mais c’est glauque”, “quand tu vas rentrer chez toi tu vas être en dépression…”, “c’est bizarre”.
                            Ça m’a surtout permis de me détacher de ce que je pensais qui me ferait peur et au final j’en ai beaucoup moins peur."</p>
                            <a href="#!"><img class="img-fluid image-marge2" src="images/cimetière 2.jpg" alt="..." /></a>
                            <span class="caption text-muted">Photo prise par <a href="https://www.flickr.com/photos/141505599@N08/page5">Catherine Marion</a></span>
                        <p style="text-decoration: underline;"><u>Maël :</u></p>
                        <div class="texte-marge3">
                            "On sait que dans d’autre pays la mort et beaucoup moins tabou et beaucoup plus festif, pensez vous que cela devrait être le cas en France ?"</p>
                        <p style="text-decoration: underline;"><u>Sarah :</u></p>
                        <div class="texte-marge3">
                            "On espère que ça se démocratise plus dans le temps, que ce soit moins réglementé. Je pense notamment dans les pays musulmans où les défunts culturellement sont enterrés sans cercueil. Nous ici, un cercueil est obligatoire. Donc dans ce cas de figure, on aimerait que ça change."</p>
                        <p style="text-decoration: underline;"><u>Maël :</u></p>
                        <div class="texte-marge3">
                            "Vous êtes en relation avec tous les cimetières ?"</p>
                        <p style="text-decoration: underline;"><u>Sarah :</u></p>
                        <div class="texte-marge3">
                                "Oui il existe des fausses temporaires pour effectuer des reprises de concessions."</p>
                                <br>
                                <br>
                        <p style="text-decoration: underline;"><u>Conclusion :</u></p>
                        <div class="texte-marge3">
                            Nous avons pu en apprendre davantage sur le métier de conseiller funéraire qui reste très tabou au sein de notre société. Cet échange a été très enrichissant et nous a fournit beaucoup d’éléments afin d’enrichir notre sujet principal : Les cimetières de Bordeaux.</p>
                               
